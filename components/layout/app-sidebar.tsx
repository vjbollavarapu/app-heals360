interface AppSidebarProps {
  isCollapsed: boolean
}

export function AppSidebar({ isCollapsed }: AppSidebarProps) {
  const [user, setUser] = useState<User | null>(null)
  const [navigationItems, setNavigationItems] = useState<any[]>([])
  const pathname = usePathname()

  useEffect(() => {
    const authService = AuthService.getInstance()
    const currentUser = authService.getCurrentUser()
    if (currentUser) {
      setUser(currentUser)
      setNavigationItems(getNavigationForRole(currentUser.role))
    }
  }, [])

  if (!user) return null

  return (
    <div
      className={cn(
        "fixed left-0 top-16 h-[calc(100vh-4rem)] bg-card border-r border-border transition-all duration-300 z-30",
        isCollapsed ? "w-16" : "w-64",
      )}
    >
      <div className="flex flex-col h-full">
        {/* User Info */}
        <div className="p-4 border-b border-border">
          <div className="flex items-center space-x-3">
            <div className="w-8 h-8 rounded-full bg-primary/10 flex items-center justify-center">
              <span className="text-sm font-medium text-primary">{user.name.charAt(0)}</span>
            </div>
            {!isCollapsed && (
              <div className="flex-1 min-w-0">
                <p className="text-sm font-medium truncate">{user.name}</p>
                <p className="text-xs text-muted-foreground truncate">{user.department || user.role}</p>
              </div>
            )}
          </div>
        </div>

        {/* Navigation */}
        <nav className="flex-1 p-2 space-y-1 overflow-y-auto">
          {navigationItems.map((item) => {
            const isActive = pathname === item.href || pathname.startsWith(item.href + "/")
            const Icon = icons[item.icon as keyof typeof icons] || icons.dashboard

            return (
              <Link key={item.id} href={item.href}>
                <Button
                  variant={isActive ? "secondary" : "ghost"}
                  className={cn(
                    "w-full justify-start h-10",
                    isCollapsed ? "px-2" : "px-3",
                    isActive && "bg-primary/10 text-primary border-primary/20",
                  )}
                >
                  <div className="flex items-center space-x-3">
                    {Icon}
                    {!isCollapsed && <span className="text-sm">{item.label}</span>}
                  </div>
                </Button>
              </Link>
            )
          })}
        </nav>
      </div>
    </div>
  )
}
